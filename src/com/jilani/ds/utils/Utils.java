package com.jilani.ds.utils;

public class Utils {
	
	public static void printArray(int[] arr) {
		for ( int i: arr)
			System.out.print(" " + i);
		System.out.println();
	}
	
	public static void printArray(char[] arr) {
		for ( char i: arr)
			System.out.print(" " + i);
		System.out.println();
	}

}
